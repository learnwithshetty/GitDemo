package Fresh;

public class DataTypee {

	public static void main(String[] args) {
		
		byte byteData=88;
		int intData = 20;
		short shortData = 6000;
		long longData = 20000000000L;
		float floatData = 1.1f;
		double doubleData = 29.948d;
		boolean booleanData = true;
		char charData = 'A';
		
		System.out.println("Value Declared is " + byteData);
		System.out.println("Value Declared is " + intData);
		System.out.println("Value Declared is " + shortData);
		System.out.println("Value Declared is " + longData);
		System.out.println("Value Declared is " + floatData);
		System.out.println("Value Declared is " + doubleData);
		System.out.println("Value Declared is " + booleanData);
		System.out.println("Value Declared is " + charData);
	}

}
