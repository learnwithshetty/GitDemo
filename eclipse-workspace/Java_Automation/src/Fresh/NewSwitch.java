package Fresh;

public class NewSwitch {

	public static void main(String[] args) {
		
		int day = 10;
		switch(day)
		{
		case 1 : System.out.println("Sunday");   // Break keyword is used to exit from the loop immeditaley
		break;
		case 2 : System.out.println("Monday");   // Break keyword is used to exit from the loop immeditaley
		break;
		case 3 : System.out.println("Tuesday");   // Break keyword is used to exit from the loop immeditaley
		break;
		case 4 : System.out.println("wednesday");   // Break keyword is used to exit from the loop immeditaley
		break;
		case 5 : System.out.println("Thursday");   // Break keyword is used to exit from the loop immeditaley
		break;
		case 6 : System.out.println("Friday");   // Break keyword is used to exit from the loop immeditaley
		break;
		case 7 : System.out.println("Saturday");   // Break keyword is used to exit from the loop immeditaley
		break;
		default : System.out.println("Nothing");
		}

	}

}
